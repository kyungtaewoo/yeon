// ============================================================
// 60간지 (六十甲子)
// ============================================================
export const SIXTY_JIAZI: Array<{ stem: HeavenlyStem; branch: EarthlyBranch }> = (() => {
  const result: Array<{ stem: HeavenlyStem; branch: EarthlyBranch }> = [];
  for (let i = 0; i < 60; i++) {
    result.push({
      stem: HEAVENLY_STEMS[i % 10],
      branch: EARTHLY_BRANCHES[i % 12],
    });
  }
  return result;
})();

